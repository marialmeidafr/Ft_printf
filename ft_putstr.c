#include "ft_printf.h"

int	ft_putstr(char *str)
{
	int	i;

	i = 0;
	if (str == NULL)
		return (write(1, "(null)", 6), 6);
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
	return (i);
}